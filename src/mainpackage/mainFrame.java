    private void bottonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottonEqualActionPerformed
        String equal;
        String disText=textField.getText();
        firstNum= Double.parseDouble(disText.substring(0,disText.indexOf(" ")));
       secNum=Double.parseDouble(disText.substring(disText.indexOf(" ")+2,disText.length()));
        if(operation=="+")
        {
            result= firstNum+secNum;
            equal= String.format("%.0f",result);
            textField.setText(equal);
            
        }
        else if(operation=="-")
        {
            result= firstNum-secNum;
            equal= String.format("%.0f",result);
            textField.setText(equal);
            
        }
        else if(operation=="*")
        {
            result= firstNum*secNum;
            equal= String.format("%.0f",result);
            textField.setText(equal);
            
        }
        else if(operation=="/")
        {
            result= firstNum/secNum;
            equal= String.format("%f",result);
            textField.setText(equal);
            
        }
    }//GEN-LAST:event_bottonEqualActionPerformed

